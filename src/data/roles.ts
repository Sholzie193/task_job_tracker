export const BENCHMARK_ROLE_COUNT = roleBenchmarks.length;
export const BENCHMARK_TASK_COUNT = roleBenchmarks.reduce(
  (count, role) => count + role.tasks.length,
  0,
);

export function getRoleBenchmarkById(roleId: string) {
  return roleBenchmarks.find((role) => role.id === roleId) ?? null;
}

export function getTaskConfigById(roleId: string, taskId: string) {
  return (
    roleBenchmarks
      .find((role) => role.id === roleId)
      ?.tasks.find((task) => task.id === taskId) ?? null
  );
}
